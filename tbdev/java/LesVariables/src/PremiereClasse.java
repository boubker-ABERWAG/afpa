public class PremiereClasse {

	public static void main(String[] args) {
		//Afficher une chaine de caractères
		System.out.println("Bonjour");
		System.out.println("Bonsoir");

		// Afficher un nombre entier
		System.out.println(12);
		
		//afficher un nombre avec une ,
		System.out.println(12.2);
		
		//afficher Un char
		System.out.println('a');		
		
		// String -> chaine de caractères
		// int -> Entier
		// float -> nombre décimal
		// char -> caractère
		// boolean -> true ou false
		
		//Afficher un boolean
		System.out.println(true);
		
		// Resultat d'un clacul
		System.out.println(12 * 12);
		System.out.println(12 - 12);
		System.out.println(12 + 12);
		System.out.println(12 / 12);
		
		//Afficher plusieurs données
		System.out.println("Bonjour " + "monsieur");
		System.out.println("Bonjour " + "monsieur " + 124);
		
		System.out.println("12 * 12 = " + 12 * 12);
		System.out.println("12 - 12 = " + (12 - 12));
		System.out.println("12 + 12 = " + (12 + 12));
		System.out.println();
		System.out.println("12 / 12 = " + 12 / 12);
		
		System.out.println();
		
		System.out.println("Utilisation de System.out.print");
		System.out.print("bonjour");
		System.out.print("bonsoir");
		
		// System.out.println("Bonjour");  =================  System.out.print("Bonjour"); System.out.println();
		System.out.println();
		System.out.print("LA variable entier1 * la variable entier2  = " + (12 * 12));
		
		
			
	}
}